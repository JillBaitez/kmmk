const __HTOS_SESSIONS = (self.__HTOS_SESSIONS = self.__HTOS_SESSIONS || {});

function buildSynthesisPrompt(originalPrompt, otherResults, selfProviderId) {
  // Mirrors the structured reasoning pattern used in bootstrap
  const other = (otherResults || []).filter(
    (r) => r.providerId !== selfProviderId
  );
  const block = other
    .map((r) => `Response from ${r.providerId}:\n${r.text || ""}`)
    .join("\n\n---\n\n");
  return `You are a synthesis agent operating in a continuation context. You have access to your own output from the batch stage via this tab's persistent chat history. You are now tasked with synthesizing an improved version by comparing your output to one or more outputs generated by other models.

Follow this structured reasoning pattern:
1. Analyze the provided output(s) from other models. Identify strengths, weaknesses, redundancies, contradictions, and areas of unique insight.
2. Evaluate how your own batch-stage output (available in this chat) compares. Determine which ideas, phrasings, or reasoning steps are stronger, clearer, or better aligned with the user's goals.
3. Integrate the best elements from all available outputs — including your own — into a single, superior synthesis. Do not privilege your own output unless it is demonstrably stronger.
4. Refine the final result for clarity, coherence, and strategic alignment with the Hybrid Thinking OS principles (e.g., multi-perspective reasoning, insight density, goal alignment).

Original user prompt: "${originalPrompt}"

--- Other Model Outputs for Comparison ---
${block}
--- End of Other Model Outputs ---

Begin Synthesis:`;
}

// Port connection handler for popup communication
chrome.runtime.onConnect.addListener((port) => {
  console.log("[htos] Port connected:", port.name);

  if (port.name === "htos-popup") {
    port.onMessage.addListener(async (message) => {
      console.log("[htos] Message from popup:", message);

      if (message.type === "sendPrompt") {
        const { prompt, providers } = message;

        // Send acknowledgment
        port.postMessage({
          type: "result",
          data: {
            providerId: "system",
            text: `Processing request for ${providers.length} provider(s)...`,
            ok: true,
          },
        });

        // Prefer orchestrator for batch prompting if initialized
        try {
          if (self.orchestrator) {
            const includeProviderIds = (providers || []).map((p) =>
              String(p).toLowerCase()
            );
            const res = await self.orchestrator.batchPrompt(prompt, {
              includeProviderIds,
              onPartial: (pid, chunk) => {
                try {
                  if (chunk && chunk.partial) {
                    port.postMessage({
                      type: "result",
                      data: {
                        providerId: pid,
                        text: chunk.text,
                        ok: true,
                        partial: true,
                      },
                    });
                  }
                } catch (_) {}
              },
            });
            // Emit final results
            for (const r of res?.raw || []) {
              port.postMessage({
                type: "result",
                data: {
                  providerId: r.providerId,
                  text: r.text,
                  ok: r.ok,
                  partial: false,
                },
              });
            }
            return;
          }
        } catch (e) {
          console.warn(
            "[htos] orchestrator path failed, falling back to manual",
            e
          );
        }

        // Process each provider with real implementations
        for (const provider of providers) {
          try {
            let adapter;

            // Get the appropriate adapter
            switch (provider.toLowerCase()) {
              case "claude":
                adapter = self.claudeAdapter;
                break;
              case "gemini":
                adapter = self.geminiAdapter;
                break;
              case "chatgpt":
                adapter = self.chatgptAdapter;
                break;
              default:
                throw new Error(`Unknown provider: ${provider}`);
            }

            if (!adapter) {
              throw new Error(`Provider ${provider} not initialized`);
            }

            // Create request object
            const request = {
              originalPrompt: prompt,
              meta: {},
            };

            // Send prompt to real provider
            const result = await adapter.sendPrompt(
              request,
              (chunk) => {
                // Handle streaming chunks if supported
                if (chunk.partial) {
                  port.postMessage({
                    type: "result",
                    data: {
                      providerId: provider,
                      text: chunk.text,
                      ok: true,
                      partial: true,
                    },
                  });
                }
              },
              new AbortController().signal
            );

            // Send final result
            port.postMessage({
              type: "result",
              data: {
                providerId: provider,
                text: result.text,
                ok: result.ok,
                partial: false,
              },
            });
          } catch (error) {
            console.error(`[htos] Error with provider ${provider}:`, error);
            port.postMessage({
              type: "result",
              data: {
                providerId: provider,
                text: error.message || "Provider error occurred",
                ok: false,
              },
            });
          }
        }
      }
    });

    port.onDisconnect.addListener(() => {
      console.log("[htos] Port disconnected:", port.name);
    });
  }
});

// Open dedicated UI tab on action click
try {
  if (chrome.action && chrome.tabs) {
    chrome.action.onClicked.addListener(async () => {
      try {
        const url = chrome.runtime.getURL("ui/index.html");
        const existing = await chrome.tabs.query({ url: [url] });
        if (existing && existing.length > 0) {
          const t = existing[0];
          if (t.id) await chrome.tabs.update(t.id, { active: true });
          if (t.windowId != null)
            await chrome.windows.update(t.windowId, { focused: true });
        } else {
          await chrome.tabs.create({ url });
        }
      } catch (e) {
        console.error("[htos] Failed to open UI tab:", e);
      }
    });
  }
} catch (e) {
  console.warn("[htos] action click handler init failed", e);
}

