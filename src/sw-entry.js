// Message handler for other extension communication
chrome.runtime.onMessage.addListener((request, sender, sendResponse) => {
  console.log("[htos] Message received:", request, "from:", sender);

  // Handle CSP bypass request
  if (request.type === "bypassCSP" && sender.tab) {
    // TODO: Implement CSP bypass logic
    sendResponse({ success: true });
  }

  // Handle UI workflow execution
  if (request.type === "EXECUTE_WORKFLOW" && request.payload) {
    (async () => {
      const workflow = request.payload || {};
      const context = workflow.context || {};
      const steps = Array.isArray(workflow.steps) ? workflow.steps : [];
      const isSynthesis =
        steps.length === 1 && steps[0]?.stepId === "synthesis_step";

      /** Returns provider adapter by id */
      const getAdapter = (providerId) => {
        switch (String(providerId).toLowerCase()) {
          case "claude":
            return self.claudeAdapter;
          case "gemini":
            return self.geminiAdapter;
          case "chatgpt":
            return self.chatgptAdapter;
          default:
            return null;
        }
      };

      try {
        const sessionId = context.sessionId || `sid-${Date.now()}`;
        const session = (__HTOS_SESSIONS[sessionId] = __HTOS_SESSIONS[
          sessionId
        ] || { originalPrompt: "", providers: {} });

        if (isSynthesis && self.orchestrator) {
          const synthesisProvider = steps[0]?.provider;
          // Gather original prompt and other results from session (fallback to variables)
          const originalPrompt =
            session.originalPrompt ||
            context?.variables?.originalPrompt ||
            steps[0]?.payload?.prompt ||
            "";
          let otherResults = Object.entries(session.providers).map(
            ([pid, v]) => ({ providerId: pid, text: v?.text || "" })
          );
          if (
            otherResults.length === 0 &&
            context?.variables?.allBatchResults
          ) {
            otherResults = Object.entries(
              context.variables.allBatchResults
            ).map(([pid, text]) => ({ providerId: pid, text: String(text) }));
          }
          // Continuation tokens to preserve provider context
          const meta = {};
          if (synthesisProvider === "claude") {
            meta.chatId = session.providers?.claude?.meta?.chatId || undefined;
          } else if (synthesisProvider === "gemini") {
            meta.cursor = session.providers?.gemini?.meta?.cursor || undefined;
          }
          const res = await self.orchestrator.batchPrompt(originalPrompt, {
            synthesis: {
              only: true,
              providerId: synthesisProvider,
              otherResults,
              meta,
            },
            onPartial: (_pid, chunk) => {
              try {
                if (chunk && chunk.partial) {
                  chrome.runtime.sendMessage({
                    type: "SYNTHESIS_PARTIAL",
                    sessionId,
                    payload: {
                      provider: synthesisProvider,
                      text: chunk.text || "",
                    },
                  });
                }
              } catch (e) {
                console.warn("[htos] Failed to stream synthesis partial", e);
              }
            },
          });
          const s = res?.synthesis || null;
          // Persist synthesis result back into session
          session.providers[synthesisProvider] = {
            text: s?.text || "",
            meta: s?.meta || {},
          };
          chrome.runtime.sendMessage({
            type: "SYNTHESIS_COMPLETE",
            sessionId,
            payload: [{ provider: synthesisProvider, response: s?.text || "" }],
          });
        } else {
          // Batch fan-out via orchestrator; store continuation tokens and outputs
          const originalPrompt =
            context?.variables?.originalPrompt ||
            steps[0]?.payload?.prompt ||
            "";
          if (!session.originalPrompt)
            session.originalPrompt = String(originalPrompt || "");

          if (self.orchestrator) {
            const includeProviderIds = steps.map((step) =>
              String(step.provider).toLowerCase()
            );
            const res = await self.orchestrator.batchPrompt(originalPrompt, {
              includeProviderIds,
            });
            // Store for continuation
            for (const r of res?.raw || []) {
              session.providers[r.providerId] = {
                text: r?.text || "",
                meta: r?.meta || {},
              };
            }
            // Build step results preserving order
            const stepResults = steps.map((step) => {
              const r = (res?.raw || []).find(
                (x) => x.providerId === step.provider
              );
              if (!r || r.ok === false) {
                return {
                  stepId: step.stepId,
                  status: "failed",
                  error: r?.errorCode || "provider_failed",
                };
              }
              return {
                stepId: step.stepId,
                status: "completed",
                result: { response: r.text ?? "" },
              };
            });
            chrome.runtime.sendMessage({
              type: "WORKFLOW_COMPLETE",
              sessionId,
              payload: { stepResults },
            });
          } else {
            // Fallback: previous manual loop
            const stepResults = [];
            for (const step of steps) {
              try {
                const adapter = getAdapter(step.provider);
                if (!adapter)
                  throw new Error(`Unsupported provider: ${step.provider}`);
                const req = { originalPrompt, meta: {} };
                const res = await adapter.sendPrompt(
                  req,
                  undefined,
                  new AbortController().signal
                );
                session.providers[step.provider] = {
                  text: res?.text || "",
                  meta: res?.meta || {},
                };
                if (res?.ok === false)
                  stepResults.push({
                    stepId: step.stepId,
                    status: "failed",
                    error: res?.errorCode || "provider_failed",
                  });
                else
                  stepResults.push({
                    stepId: step.stepId,
                    status: "completed",
                    result: { response: res?.text ?? "" },
                  });
              } catch (err) {
                console.error("[htos] Workflow step failed", step?.stepId, err);
                stepResults.push({
                  stepId: step?.stepId || "unknown",
                  status: "failed",
                  error: err?.message || String(err),
                });
              }
            }
            chrome.runtime.sendMessage({
              type: "WORKFLOW_COMPLETE",
              sessionId,
              payload: { stepResults },
            });
          }
        }
      } catch (e) {
        console.error("[htos] Failed to execute workflow", e);
      }
    })();

    // No async sendResponse used
    return false;
  }

  // Minimal history endpoints to keep UI stable (return empty data)
  if (request.type === "GET_FULL_HISTORY") {
    try {
      sendResponse && sendResponse({ success: true, data: { sessions: [] } });
    } catch (_) {}
    return true;
  }
  if (request.type === "GET_HISTORY_SESSION") {
    try {
      sendResponse &&
        sendResponse({
          success: true,
          data: {
            id: request.payload?.sessionId,
            sessionId: request.payload?.sessionId,
            title: "Session",
            messages: [],
          },
        });
    } catch (_) {}
    return true;
  }
  if (request.type === "GET_LAST_ESCALATION") {
    try {
      sendResponse && sendResponse({ success: true, data: null });
    } catch (_) {}
    return true;
  }

  // Return true to indicate async response
  return true;
});
