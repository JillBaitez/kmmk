const OffscreenController = {
  async init() {
    console.log('[Service Worker] Initializing offscreen document controller...');
    await this._createOffscreenPageIfMissing();
  },

  async _createOffscreenPageIfMissing() {
    try {
      const hasDocument = await chrome.offscreen.hasDocument();
      if (!hasDocument) {
        console.log('[Service Worker] No offscreen document found. Creating one...');
        await chrome.offscreen.createDocument({
          url: 'offscreen.html',
          reasons: [chrome.offscreen.Reason.BLOBS, chrome.offscreen.Reason.DOM_PARSER],
          justification: 'HTOS needs offscreen DOM for image/canvas and stable bus iframe',
        });
        console.log('[Service Worker] Offscreen document created successfully.');
      } else {
        console.log('[Service Worker] Offscreen document already exists.');
      }
    } catch (error) {
      console.error('[Service Worker] Failed to create offscreen document:', error);
      throw error;
    }
  }
};

